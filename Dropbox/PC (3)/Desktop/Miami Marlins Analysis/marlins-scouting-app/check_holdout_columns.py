import pandas as pd

def check_holdout_columns():
    """Check what columns are available in the holdout data"""
    try:
        df = pd.read_csv('ronald_acuna_jr_holdout_statcast.csv')
        print("Columns in holdout data:")
        print("=" * 50)
        for i, col in enumerate(df.columns):
            print(f"  {i+1:2d}. {col}")
            
        print(f"\nTotal columns: {len(df.columns)}")
        
        # Check for swing-related columns
        swing_cols = [col for col in df.columns if 'swing' in col.lower()]
        if swing_cols:
            print(f"\nSwing-related columns: {swing_cols}")
        else:
            print("\nNo swing-related columns found")
            
        # Check for event-related columns
        event_cols = [col for col in df.columns if 'event' in col.lower()]
        if event_cols:
            print(f"Event-related columns: {event_cols}")
        else:
            print("No event-related columns found")
            
    except Exception as e:
        print(f"Error loading holdout data: {e}")

if __name__ == "__main__":
    check_holdout_columns() 

