import pickle
import pandas as pd

# Load and inspect the models
try:
    with open('sequential_models.pkl', 'rb') as f:
        models = pickle.load(f)
    
    print("✅ Models loaded successfully")
    print(f"Model keys: {list(models.keys())}")
    
    if 'swing_features' in models:
        print(f"Number of swing features: {len(models['swing_features'])}")
        print("First 10 features:", models['swing_features'][:10])
    
    if 'swing_preprocessor' in models:
        print("✅ Swing preprocessor found")
    
    if 'swing_model' in models:
        print("✅ Swing model found")
        
except FileNotFoundError:
    print("❌ sequential_models.pkl not found")
except Exception as e:
    print(f"❌ Error loading models: {e}") 
