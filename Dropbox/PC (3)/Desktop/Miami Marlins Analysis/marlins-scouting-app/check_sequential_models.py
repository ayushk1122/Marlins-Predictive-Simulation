import joblib
import pandas as pd

# Load the sequential models
try:
    models = joblib.load('sequential_models.pkl')
    print("✓ Loaded sequential_models.pkl")
    print(f"Type: {type(models)}")
    
    if isinstance(models, dict):
        print(f"Available keys: {list(models.keys())}")
        for key, model in models.items():
            print(f"\nModel: {key}")
            print(f"  Type: {type(model)}")
            if hasattr(model, 'predict'):
                print(f"  Has predict method: ✓")
            if hasattr(model, 'predict_proba'):
                print(f"  Has predict_proba method: ✓")
    else:
        print(f"Model structure: {models}")
        
except Exception as e:
    print(f"✗ Error loading sequential_models.pkl: {e}")

